package model;

/**
 *
 * @author Dell
 */
public class Slot {
    private int slotID;
    private String timeSlot;

    public Slot() {
    }

    public Slot(int slotID, String timeSlot) {
        this.slotID = slotID;
        this.timeSlot = timeSlot;
    }

    public int getSlotID() {
        return slotID;
    }

    public void setSlotID(int slotID) {
        this.slotID = slotID;
    }

    public String getTimeSlot() {
        return timeSlot;
    }

    public void setTimeSlot(String timeSlot) {
        this.timeSlot = timeSlot;
    }
    
    
}
